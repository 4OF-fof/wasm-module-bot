use std::{fs, path::PathBuf};

use patchouli_plugin_api::{
    ActionPlan, BotEvent, Capability, DiscordEmbed, DiscordEmbedField, DiscordManifest,
    EffectRequest, EffectResult, HttpMethod, PluginManifest, PluginModuleInfo, SlashCommand,
    TriggerGroup, TriggerSource,
};
use ts_rs::{Config, TS};

const HEADER: &str = "// Generated by patchouli-plugin-api. Do not edit by hand.\n";

fn main() {
    let output = [
        HEADER.to_string(),
        declaration::<PluginManifest>(),
        declaration::<TriggerGroup>(),
        declaration::<TriggerSource>(),
        declaration::<Capability>(),
        declaration::<HttpMethod>(),
        declaration::<DiscordManifest>(),
        declaration::<SlashCommand>(),
        declaration::<PluginModuleInfo>(),
        declaration::<BotEvent>(),
        declaration::<ActionPlan>(),
        declaration::<EffectRequest>(),
        declaration::<DiscordEmbed>(),
        declaration::<DiscordEmbedField>(),
        declaration::<EffectResult>(),
    ]
    .join("\n");

    let path =
        PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../../../host/src/generated/plugin-api.ts");

    fs::write(&path, output).unwrap_or_else(|error| {
        panic!("failed to write {}: {error}", path.display());
    });
}

fn declaration<T: TS>() -> String {
    format!("export {}\n", T::decl(&Config::new()))
}
